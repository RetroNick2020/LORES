unsigned char *tilemap[16][16];
/*
 * Build map from text representation
 *  . = grass
 *  * = tree
 *  # = dirt
 *  ~ = water
 */
unsigned char *map[] = {"................",
                        "..***...###.....",
                        ".****..##.###...",
                        "***~**.#....###.",
                        "**~~~*.#...~~~#~",
                        "~~~?~~~#~~~~..#.",
                        "*~~~**.#....###.",
                        ".****..#..###...",
                        ".****..####.....",
                        "..**..##........",
                        ".*...##..****...",
                        ".#####..*....*..",
                        "##.*...*.#..#.*.",
                        "#......*......*.",
                        "##.*....*.##.*..",
                        ".###.....****..."};
void buildmap(void)
{
    int row, col;
    unsigned char *tileptr;

    for (row = 0; row < 16; row++)
    {
        for (col = 0; col < 16; col++)
        {
            switch (map[row][col])
            {
                case '.':
                    tileptr = grass;
                    break;
                case '*':
                    tileptr = tree;
                    break;
                case '#':
                    tileptr = dirt;
                    break;
                case '~':
                    tileptr = water;
                    break;
                default:
                    tileptr = testile;
            }
            tilemap[row][col] = tileptr;
        }
    }
}
/*
 * Demo tiling and scrolling screen
 */
int main(int argc, char **argv)
{
    unsigned int s, t;
    unsigned long st;
    int scrolldir;
    unsigned char sprite[10][5];

    buildmap();
    gr160(0);
    /*
     * Set initial coordinates and scroll direction.
     * Scroll increment must be power of two.
     */
    s = 2;
    t = 8;
    scrolldir = SCROLL_RIGHT | SCROLL_UP;
    /*
     * Draw full screen at start address
     */
    tileInit(s, t, 16, 16, (unsigned char **)tilemap);
    /*
     * Use hardware scrolling
     */
    while (!kbhit())
    {
        /*
         * Change scroll direction at map boundaries
         */
        if (s == ((16 << 4) - 162) || s == 0) scrolldir ^= SCROLL_LEFT | SCROLL_RIGHT;
        if (t == ((16 << 4) - 102) || t == 0) scrolldir ^= SCROLL_UP   | SCROLL_DOWN;
        /*
         * Move origin based on scroll direction
         */
        if (scrolldir & SCROLL_LEFT)
            s += 2;
        else if (scrolldir & SCROLL_RIGHT)
            s -= 2;
        if (scrolldir & SCROLL_UP)
            t += 2;
        else if (scrolldir & SCROLL_DOWN)
            t -= 2;
        /*
         * Place sprite in middle of screen
         */
        tileBuf(s + 80-4, t + 50-4, 10, 10, sprite[0]);
        sprite[2][1] = 0xFF;
        sprite[3][1] = 0xFF;
        sprite[2][3] = 0xFF;
        sprite[3][3] = 0xFF;
        sprite[6][1] = 0xFF;
        sprite[7][1] = 0xFF;
        sprite[6][3] = 0xFF;
        sprite[7][3] = 0xFF;
        st = tileScroll(scrolldir);
        s  = st;
        t  = st >> 16;
        cpyBuf(s + 80-4, t + 50-4, 10, 10, sprite[0]);
        //if (getch() == 'Q') {txt80(); return 0;}
    }
    getch();
    txt80();
    return 0;
}
