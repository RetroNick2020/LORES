void spriteBuf(int x, int y, int width, int height, unsigned char *spriteptr, int span, unsigned char *bufptr)
{
    int w;
    unsigned char pix;

    bufptr += y * span + (x >> 1);
    width >>= 1;
    span   -= width;
    if (x & 1)
    {
        while (height--)
        {
            for (w = 0; w < width; w++)
            {
                pix = *spriteptr & 0x0F;
                if (pix != 0x08)
                    *bufptr = (*bufptr & 0x0F) | (pix << 4);
                bufptr++;
                pix = *spriteptr++ & 0xF0;
                if (pix != 0x80)
                    *bufptr = (*bufptr & 0xF0) | (pix >> 4);
            }
            bufptr += span;
        }
    }
    else
    {
        while (height--)
        {
            for (w = 0; w < width; w++)
            {
                pix = *spriteptr & 0x0F;
                if (pix != 0x08)
                    *bufptr = (*bufptr & 0xF0) | pix;
                pix = *spriteptr++ & 0xF0;
                if (pix != 0x80)
                    *bufptr = (*bufptr & 0x0F) | pix;
                bufptr++;
            }
            bufptr += span;
        }
    }
}
